public class _02CreatingArray2ndMethod {
    
    public static void main(String[] args) {
        
        int nums[] = new int[4];

        nums[0] = 5;
        nums[1] = 4;
        nums[2] = 3;
        nums[3] = 2;

        for(int i=0; i<=3; i++){
            System.out.println(nums[i]);
        }

    }
}
