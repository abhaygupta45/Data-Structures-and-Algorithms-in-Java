public class _01Creating1DArray {
    public static void main(String args[]){

        int nums[] = {2,4,5,6,7};
        System.out.println(nums[3]);//print the value of index number 3 which is 6

        //print all values using loop
        for (int i=0; i<5; i++){
            System.out.println(nums[i]);
        }
    }
}