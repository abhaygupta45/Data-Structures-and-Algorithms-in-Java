public class _02RelationalOperators {
    public static void main (String[] args){
        int a = 10;
        int b = 20;
        boolean result = a == b;
        boolean result1 = a >= b;
        boolean result2 = a <= b;
        System.out.println(result);
        System.out.println(result1);
        System.out.println(result2);
    }
    
}
