public class _02Program {
    
    public static void main(String[] args) {
        
        int num = 200;

        switch(num){
            case 200: System.out.println("case1");
            case 100: System.out.println("case2");
            case 300: System.out.println("case3");
            default : System.out.println("default");
        }
    }
}