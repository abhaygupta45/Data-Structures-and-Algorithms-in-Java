public class _08numeric_DataTypes {

    public static void main (String []args){

        // byte type
        byte num = 10;
        System.out.println("byte data type" + num);

        //short type 
        short num1 = 20;
        System.out.println("short data type "+ num1);

        //int type
        int num2 = 20;
        System.out.println("int data type" +num2);

        //long data type
        long num3 = 40L;
        System.out.println("long data type"+num3);
    }
}
